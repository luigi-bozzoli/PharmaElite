package model.beans;

public class MetodoDiSpedizioneBean {
	private String numTracking, dataArrivo, dettagli, idOrdine, emailCliente;
	
	public MetodoDiSpedizioneBean() {}

	public String getNumTracking() {
		return numTracking;
	}

	public void setNumTracking(String numTracking) {
		this.numTracking = numTracking;
	}

	public String getDataArrivo() {
		return dataArrivo;
	}

	public void setDataArrivo(String dataArrivo) {
		this.dataArrivo = dataArrivo;
	}

	public String getDettagli() {
		return dettagli;
	}

	public void setDettagli(String dettagli) {
		this.dettagli = dettagli;
	}

	public String getIdOrdine() {
		return idOrdine;
	}

	public void setIdOrdine(String idOrdine) {
		this.idOrdine = idOrdine;
	}

	public String getEmailCliente() {
		return emailCliente;
	}

	public void setEmailCliente(String emailCliente) {
		this.emailCliente = emailCliente;
	}
	
	
}
